public class SupperClass {
    private String name;
    private int nom;

    public SupperClass(String name, int nom) {
        this.name = name;
        this.nom = nom;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getNom() {
        return nom;
    }

    public void setNom(int nom) {
        this.nom = nom;
    }
}
